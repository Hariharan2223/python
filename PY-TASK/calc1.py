def add(a,b):
    """Addition function"""
    return a+b

def sub(a,b):
    """Subtraction  function"""
    c=a-b
    if(c>=0):
        return c
    else:
        return -c
     

def mul(a,b):
    """Multiplication function"""
    return a*b

def div(a,b):
    """division function"""
    return a/b
def percentage(a,b):
    """percentage function"""
    return b*(a/100)




